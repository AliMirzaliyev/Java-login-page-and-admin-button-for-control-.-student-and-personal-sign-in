    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        PreparedStatement ps;
        ResultSet rs;

        String Name = name.getText();
        String Surname = surname.getText();
        String idnumber = idNumber.getText();
        String pasword = password.getText();
        String depar = department.getText();

        String query = "insert into student values (?,?,?,?,?)";

        try {
            ps = MyConnection.getConnection().prepareStatement(query);

            ps.setString(1, Name);

            ps.setString(2, Surname);

            ps.setString(3, idnumber);
            

            ps.setString(4, depar);

            ps.setString(5, pasword);

            ps.executeUpdate();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error, cannot save " +ex);
            System.out.println(ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

